function draw(){

    fill(255, 10);
    rect(0, 0, width, height);
    fill(0);
    
    for(var i = 0; i < balls.length; i++){
        balls[i].display();
        balls[i].update(mouseX, mouseY);
    }
    
}


function makeGrid(){
    for(var i = 0; i < height; i+=10){
        var labels = counter % 5;
        if(labels === 0){ 
            text(i,0, i);
            stroke(100, 100);
        }else{
            stroke(100, 30);
        }
        line(0, i, width, i);    
        line(i, 0, i, height);
        
        counter++;
    }
}


class Ball{
    
    constructor(tx, ty, tr){
        this.x =  tx;
        this.y = ty;
//        this.mx = mouseX;
        this.r = tr;
    }
    
    display(){
        ellipse(this.x, this.y, this.r, this.r);
    }
    update(mx, my){
        
        var d = dist(this.x, this.y, mx, my);
        var mapR = map(d, 0, displayWidth, custom.minR, custom.maxR); //for raduis
        var mapX = map(mx, 0, displayWidth, -0.5, 0.5); //for pos
        
        var computeR = mapR;
        
        ellipse(this.x, this.y, computeR, computeR);
        
    }
    
}